void loop() {
    static int beatMsec = 0; 
    int heartRateBPM = 0; 
    if (heartbeatDetected(analogPin, delayMsec)) { 
        heartRateBPM = 60000 / beatMsec; 
        digitalWrite(ledPin,1); 
    
        // Serie de données
        Serial.print("Pouls détecté: "); 
        Serial.println(heartRateBPM); 
        beatMsec = 0; 
    } else { 
        digitalWrite(ledPin,0); 
    } 
    delay(delayMsec); 
    beatMsec += delayMsec; 
}
